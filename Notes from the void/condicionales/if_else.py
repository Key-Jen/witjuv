# Estructuras condicinales, se evaluará una condicion lógica
# Existirá un set de instrucciones para una respuesta verdadera
# Luego tendremos otro set de instrucciones para una respuesta falsa

edad = 21
if edad >= 18:
    print(f"Con {edad} años, Usted, en mayor de edad.")
else:
    print(f"Con {edad} años, Usted; en menor de edad")

