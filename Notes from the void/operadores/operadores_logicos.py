# Operador logico AND o y
# Estas operaciones para obtener un resultado verdadero

print(True & True)
print(True & False)
print(False & True)
print(False & False)

# Operador logico OR o O
# Es verdadero cuando el 
print(True | True)
print(True | False)
print(False | True)
print(False | False)
print()

#Operador logico de NOT o negacion 
print(not True)
print(not False)
print()

edad = 21
menor_edad = not edad > 18
